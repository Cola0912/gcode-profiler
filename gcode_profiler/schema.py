GROUPS = {
    "printer": ("🖨  プリンター設定", PRINTER),
    "filament": ("◎  フィラメント設定", FILAMENT),
    "process": ("≣  プロセス設定", PROCESS),
}


def all_fields(schema):
    """スキーマ内の全 Field を yield する (special タブは除く)。"""
    for st in schema:
        for _sec, fields in st.sections:
            for f in fields:
                yield f


def default_values():
    """解析なしの既定値ストアと出所(全て default)を返す。"""
    values, prov = {}, {}
    for _gk, (_t, schema) in GROUPS.items():
        for f in all_fields(schema):
            if f.default is not None:
                values[f.key] = f.default
                prov[f.key] = "default"
    return values, prov


def prefill_values(result: dict):
    """解析結果から値ストアと出所を初期化する。
    返り値: (values, provenance)。
    provenance[key]: 'recovered'=G-codeから復元 / 'default'=取得できず既定値 /
                     'unknown'=不明 (値はあるが信頼性不明) / 'estimated'=低信頼度推定。
    unknown の値を recovered として表示しない。"""
    values, prov = default_values()

    def get(path):
        sec, fld = path.split(".", 1)
        return result.get(sec, {}).get(fld)

    for _gk, (_t, schema) in GROUPS.items():
        for f in all_fields(schema):
            if f.src:
                v = get(f.src)
                if v is not None:
                    values[f.key] = v
                    # low_conf フィールドは 'estimated'、それ以外は 'recovered'
                    prov[f.key] = "estimated" if f.low_conf else "recovered"

    # G-codeスタイルの推定(復元扱い)
    fs = result.get("meta", {}).get("feature_style", "")
    if "産業機" in fs:
        values["machine.gcode_flavor"] = "産業機(独自)"
        prov["machine.gcode_flavor"] = "recovered"

    return values, prov
