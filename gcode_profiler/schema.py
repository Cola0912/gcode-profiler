"""
設定項目スキーマ (OrcaSlicer のプリンター/フィラメント/プロセス設定画面を踏襲)
=============================================================================
各設定グループ(printer/filament/process)を「サブタブ → セクション → フィールド」で定義。
G-code から復元可能な項目は src(解析結果パス)を持ち、自動でプリフィルされる。

Field.visibility     : "basic" | "advanced" | "expert"
Field.recoverability : "explicit"|"runtime"|"geometry"|"estimated"|"profile_only"|"target_only"
Field.native_key     : OrcaSlicer 等の確認済みネイティブキー (None = 未確認)
Field.canonical_key  : Phase 6 向け標準キー ("" = 未設定)
"""
from __future__ import annotations

from dataclasses import dataclass, field as dc_field
from typing import Optional


@dataclass
class Field:
    key: str                            # 値ストアの一意キー ("section.field" 形式)
    label: str
    unit: str = ""
    kind: str = "num"                   # num | int | bool | text | choice | color | gcode
    choices: Optional[list] = None
    default: object = None
    src: Optional[str] = None           # 解析結果からのプリフィル元 "sec.field"
    low_conf: bool = False
    # --- Phase 5.5 拡張メタデータ ---
    native_key: Optional[str] = None    # 確認済みネイティブキー (OrcaSlicer 等)
    canonical_key: str = ""             # Phase 6 標準キー
    description: str = ""              # ツールチップ用説明
    visibility: str = "basic"           # basic | advanced | expert
    per_tool: bool = False              # ツール別に独立した値を持つか
    enabled_if: list = dc_field(default_factory=list)   # 依存フィールドキーのリスト
    visible_if: list = dc_field(default_factory=list)   # 表示条件フィールドキーのリスト
    recoverability: str = "profile_only"  # G-codeからの回復方法
    deprecated: bool = False


@dataclass
class SubTab:
    title: str
    sections: list = dc_field(default_factory=list)  # [(section_title, [Field,...]), ...]
    special: Optional[str] = None   # None / "filaments" / "gcode" / "per_filament"


