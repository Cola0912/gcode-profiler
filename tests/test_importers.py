def test_reverse_index_is_one_to_one_only():
    idx = to_canonical.build_reverse_index("orca")
    assert idx["layer_height"][0] == "process.quality.layer_height"
    assert idx["outer_wall_speed"][0] == "process.speed.outer_wall"
    # one_to_many "line_width" must not be reversed
    assert "line_width" not in idx


def test_to_canonical_maps_and_preserves_unknown():
    prof = json_family.parse_json(ORCA_PARENT, "p.json", "OrcaSlicer")
    canonical = to_canonical.to_canonical(prof, "orca", "OrcaSlicer", "2.3")
    assert m.effective_of(canonical, "process.quality.layer_height") == 0.2
    assert m.effective_of(canonical, "process.strength.sparse_infill_density") == 15.0
    # configured layer, not observed
    node = m.get_value(canonical, "process.quality.layer_height")
    assert node["configured"] == 0.2
    assert node["observed"] is None
    # unknown key preserved
    assert "some_orca_only_key" in canonical["unmapped"]


def test_to_canonical_unit_conversion_reversed():
    prof = xml_family.parse_xml(S3D_FFF, "p.fff", "Simplify3D")
    canonical = to_canonical.to_canonical(prof, "simplify3d", "Simplify3D")
    # defaultSpeed 3600 mm/min -> 60 mm/s inner_wall
    assert m.effective_of(canonical, "process.speed.inner_wall") == 60.0


