def test_inheritance_child_overrides_parent(tmp_path):
    child = json_family.parse_json(ORCA_CHILD, "child.json", "OrcaSlicer")
    parent = json_family.parse_json(ORCA_PARENT, "parent.json", "OrcaSlicer")
    repo = Repository.from_profiles([child, parent])
    resolved = inheritance.resolve(child, repo)
    # child value wins
    assert resolved.raw_fields["outer_wall_speed"].parsed_value == [120.0] or \
           resolved.raw_fields["outer_wall_speed"].parsed_value == ["120"]
    # inherited-only key present
    assert "layer_height" in resolved.raw_fields
    assert "layer_height" in resolved.inherited_keys
    assert "outer_wall_speed" in resolved.declared_keys
    assert resolved.origin_map["layer_height"] == "0.20mm Base"


def test_inheritance_missing_parent_warns():
    child = json_family.parse_json(ORCA_CHILD, "child.json", "OrcaSlicer")
    repo = Repository.from_profiles([child])   # parent absent
    resolved = inheritance.resolve(child, repo)
    assert any("親プリセット" in w for w in resolved.inheritance_warnings)


def test_inheritance_cycle_detected():
    a = json_family.parse_json(
        json.dumps({"type": "process", "name": "A", "inherits": "B", "k": "1"}),
        "a.json", "OrcaSlicer")
    b = json_family.parse_json(
        json.dumps({"type": "process", "name": "B", "inherits": "A", "k": "2"}),
        "b.json", "OrcaSlicer")
    repo = Repository.from_profiles([a, b])
    resolved = inheritance.resolve(a, repo)
    assert any("循環" in w for w in resolved.inheritance_warnings)


