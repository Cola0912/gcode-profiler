def test_import_profile_resolves_inheritance_from_siblings(tmp_path):
    _write(tmp_path, "parent.json", ORCA_PARENT)
    child_path = _write(tmp_path, "child.json", ORCA_CHILD)
    canonical = importers.import_profile(child_path)
