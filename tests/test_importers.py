# 7. merge configured + observed with conflict recording
# ===========================================================================
def _observed_result():
    return {
        "meta": {"source": "Unknown", "method": "ツールパス逆算"},
        "quality": {"layer_height": 0.2, "outer_wall_width": 0.42},
        "speed": {"outer_wall_speed": 95},     # differs from profile 120
        "strength": {"wall_loops": 3, "has_support": None, "has_raft": None},
        "temperature": {"nozzle_temp": 215},
        "retraction": {},
    }


def test_import_and_merge_records_conflict(tmp_path):
    _write(tmp_path, "parent.json", ORCA_PARENT)
    child_path = _write(tmp_path, "child.json", ORCA_CHILD)
    merged = importers.import_and_merge(child_path, _observed_result())

    node = m.get_value(merged, "process.speed.outer_wall")
    assert node["configured"] == 120.0
    assert node["observed"] == 95 or node["emitted"] == 95
    # conflict recorded
    conflict_keys = {c["canonical_key"] for c in merged["metadata"]["conflicts"]}
    assert "process.speed.outer_wall" in conflict_keys
    # effective prefers configured (profile) over observed
    assert node["effective"] == 120.0


def test_merge_agreeing_values_no_conflict(tmp_path):
    p = _write(tmp_path, "config.ini", PRUSA_INI)
    result = {
        "meta": {}, "quality": {"layer_height": 0.2},
        "speed": {}, "strength": {}, "temperature": {}, "retraction": {},
    }
    merged = importers.import_and_merge(p, result)
    node = m.get_value(merged, "process.quality.layer_height")
    assert node["configured"] == 0.2
    conflict_keys = {c["canonical_key"] for c in merged["metadata"]["conflicts"]}
    assert "process.quality.layer_height" not in conflict_keys


