def test_json_parser_keeps_percentage_distinct():
    prof = json_family.parse_json(ORCA_PARENT, "x.json", "OrcaSlicer")
    dens = prof.raw_fields["sparse_infill_density"]
    assert dens.value_mode == "percentage"
    assert dens.parsed_value == 15.0
    lh = prof.raw_fields["layer_height"]
    assert lh.parsed_value == 0.2 and lh.value_mode == "absolute"


def test_ini_parser_flat_and_comments():
    prof = ini_family.parse_ini(PRUSA_INI, "x.ini", "PrusaSlicer")
    assert "layer_height" in prof.raw_fields
    assert prof.raw_fields["fill_density"].value_mode == "percentage"
    assert any("PrusaSlicer" in c for c in prof.comments)


def test_ini_parser_bundle_splits_sections():
    bundle = ini_family.parse_ini(
        "[print:Fast]\nlayer_height = 0.3\n[filament:PLA]\ntemperature = 200\n",
        "b.ini", "PrusaSlicer")
    assert bundle.profile_kind == "bundle"
    kinds = {s.profile_kind for s in bundle.sub_profiles}
    assert kinds == {"process", "filament"}


def test_xml_parser_reads_leaf_elements():
    prof = xml_family.parse_xml(S3D_FFF, "p.fff", "Simplify3D")
