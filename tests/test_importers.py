def test_gui_model_loads_imported_profile_as_configured(tmp_path):
    from gcode_profiler.gui import Model

    p = _write(tmp_path, "config.ini", PRUSA_INI)
    canonical = importers.import_profile(p)
    model = Model()
    model.load_canonical_profile(canonical)

    assert model.values["quality.layer_height"] == 0.2
    assert model.provenance["quality.layer_height"] == "imported_profile"
    assert model.values["speed.outer_wall_speed"] == 30.0
    assert model.meta["method"] == "プロファイル読込"
    assert model.meta["unmapped_count"] >= 1

    model.values["speed.outer_wall_speed"] = 44.0
    model.provenance["speed.outer_wall_speed"] = "edited"
    edited = model.to_canonical_profile()
    node = m.get_value(edited, "process.speed.outer_wall")
    assert node["configured"] == 30.0
    assert node["edited"] == 44.0
    assert node["effective"] == 44.0


def test_export_flow_accepts_imported_canonical_profile(tmp_path):
    from gcode_profiler import export_flow

    p = _write(tmp_path, "config.ini", PRUSA_INI)
    canonical = importers.import_profile(p)
    _prof, plan = export_flow.build_plan_from_canonical(canonical, "prusa")
    assert plan["target"] == "prusa"
    assert any(e["canonical_key"] == "process.quality.layer_height"
               and e["effective_value"] == 0.2
               for e in plan["entries"])
